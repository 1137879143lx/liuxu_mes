const mongoose = require('mongoose')

const unitSchema = new mongoose.Schema({
  content: {
    type: String,
    required: true
  },
  createdBy: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
})

const Unit = mongoose.model('Unit', unitSchema)

module.exports = Unit
